void bkp_resetCollision2DReturn(BKP_Collision2DReturn * result, BKP_Vec2 mov)
{
	result->mov = mov;
	result->dirs[BKP_UP] = result->dirs[BKP_LEFT] = result->dirs[BKP_DOWN] = result->dirs[BKP_RIGHT] = BKP_FALSE; 
	result->collides = BKP_FALSE;
	result->inside = BKP_FALSE;
	return;
}

/*--------------------------------------------------------------------------------*/
BKP_Collision2DReturn bkp_createCollision2DReturn(BKP_Vec2 mov)
{
	BKP_Collision2DReturn result;
	result.mov = mov;
	result.dirs[BKP_UP] = result.dirs[BKP_LEFT] = result.dirs[BKP_DOWN] = result.dirs[BKP_RIGHT] = BKP_FALSE; 
	result.collides = BKP_FALSE;
	result.inside = BKP_FALSE;

	return result;
}
