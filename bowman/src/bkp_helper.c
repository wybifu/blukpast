float bkp_adjustPositionX(BKP_Rect pts1, BKP_Rect pts2,int dir)
{
	if(dir == BKP_LEFT)
		return (fcmp(pts1.x2, pts2.x2) > 0) ? pts1.x2 - pts1.x1 : pts2.x2 - pts2.x1; 
	else
		return (fcmp(pts1.x2, pts2.x2) < 0) ? pts1.x2 - pts1.x1 : pts2.x2 - pts2.x1; 
}

/*--------------------------------------------------------------------------------*/
float bkp_adjustPositionY(BKP_Rect pts1, BKP_Rect pts2,int dir)
{
	if(dir == BKP_UP)
		return (fcmp(pts1.y2, pts2.y2) > 0) ? pts1.y2 - pts1.y1 : pts2.y2 - pts2.y1; 
	else
		return (fcmp(pts1.y2, pts2.y2) < 0) ? pts1.y2 - pts1.y1 : pts2.y2 - pts2.y1; 
}
