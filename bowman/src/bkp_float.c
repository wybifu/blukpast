#define FLDLT 0.0000000000000005f;

/**************************************************************************
*	Structs, Enum, Unio and Typesdef	
**************************************************************************/

/**************************************************************************
*	Globals	
**************************************************************************/

float stc_hprec = FLDLT;

/***************************************************************************
*	Prototypes 	
**************************************************************************/

/**************************************************************************
*	Main	
**************************************************************************/



/**************************************************************************
*	Implementations	
**************************************************************************/

void bkp_float_setPrecision(float x)
{
	stc_hprec = x / 2;
	return;
}
/*--------------------------------------------------------------------------------*/

void bkp_float_setPrecisionDefault(void)
{
	stc_hprec = FLDLT;
	return;
}
/*--------------------------------------------------------------------------------*/

int fcmp(float a, float b)
{
	if( a + stc_hprec < b) return -1; 
	if( a - stc_hprec > b) return  1; 

	return 0;
}
